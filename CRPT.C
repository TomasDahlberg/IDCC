/*
! The string 
'Copyright 1991-1994 IVT Electronic AB'
! is encoded as;
*/
static unsigned char copyrightNotice[] = {
  0x25, 0x52, 0x3d, 0x4d, 0x34, 0x46, 0x2f, 0x48, 0x20, 0x54, 0x74, 0x45, 
  0x7c, 0x45, 0x74, 0x59, 0x68, 0x51, 0x68, 0x5c, 0x7c, 0x35, 0x63, 0x37, 
  0x17, 0x52, 0x3e, 0x5b, 0x38, 0x4c, 0x3e, 0x51, 0x3f, 0x56, 0x35, 0x15, 
  0x54, 0x16
};
/*
! Use the following procedure to decode it;
*/
void printCopyright()
{
  int seed = 17;
  int len;
  unsigned char *str;
  str = copyrightNotice;
  len = *str++;
  for (;len; len--) {
    printf("%c", *str ^ seed);
    seed = *str++;
  }
  printf("\n");
}
